using CarterGames.Shared.NotionData;

namespace CarterGames.NotionData.Editor
{
    public abstract class NotionApiRequestHandlerBase
    {
        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Properties
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */
        
        public abstract NotionApiReleaseVersion ApiVersion { get; }

        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Events
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */
        
        /// <summary>
        /// Raises when the data has been received from Notion.
        /// </summary>
        public readonly Evt<NotionRequestResult> DataReceived = new Evt<NotionRequestResult>();
        
        
        /// <summary>
        /// Raises when there was an error when trying to receive data from Notion.
        /// </summary>
        public readonly Evt<NotionRequestError> RequestError = new Evt<NotionRequestError>();

        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Methods
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */
        
        public abstract void StartDownload(NotionRequestData data);
    }
}