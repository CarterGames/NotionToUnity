using System;
using UnityEngine;

namespace CarterGames.NotionData
{
    /// <summary>
    /// A wrapper base class for converting a notion database property into something else.
    /// </summary>
    [Serializable]
    public class NotionDataWrapper<T>
    {
        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Fields
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */

        [SerializeField] protected string id;
        [SerializeField] protected T value;

        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Properties
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */

        /// <summary>
        /// The value stored in the wrapper.
        /// </summary>
        public T Value => value;
        
        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Constructors
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */

        /// <summary>
        /// Makes a new wrapper with the entered id.
        /// </summary>
        /// <param name="id"></param>
        public NotionDataWrapper(string id)
        {
            this.id = id;
            Assign();
        }

        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Methods
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */
        
        /// <summary>
        /// Assigns the reference when called.
        /// </summary>
        protected virtual void Assign()
        {
            return;
        }
    }
}